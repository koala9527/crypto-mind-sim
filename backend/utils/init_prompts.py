def init_prompts() -> None:
    init_db()
    db = SessionLocal()
    try:
        existing_count = db.query(PromptConfig).count()
        if existing_count > 0:
            logger.info("Prompt configs already exist: %s", existing_count)
            return
        for prompt_data in DEFAULT_PROMPTS:
            db.add(PromptConfig(**prompt_data))
        db.commit()
        logger.info("Initialized %s default prompts", len(DEFAULT_PROMPTS))
    except Exception as exc:
        logger.error("Init prompts failed: %s", exc)
        db.rollback()
    finally:
        db.close()


if __name__ == "__main__":
    init_prompts()
